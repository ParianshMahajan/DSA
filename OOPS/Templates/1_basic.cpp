#include<bits/stdc++.h>
using namespace std;

template <class A, class B>
void show(A a,B b){
    cout<<a<<" "<<b<<endl;
}

//actually it converts the class datatype to the datatype which is entered; so kind of type casting


int main(){
    show(23,"abc"); 
    show('a',25.6);       
    return 0;
}